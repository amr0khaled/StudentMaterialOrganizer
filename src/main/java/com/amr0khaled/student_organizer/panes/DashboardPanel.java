  private void changeBackgroundColor(String name) {
    regularBackground();
    switch (name) {
      case "lectures":
        lectures.setBackground(openedColor);
        lectureOpen = true;
      break;
      case "sections":
        sections.setBackground(openedColor);
        sectionOpen = true;
      break;
      case "assignments":
        assignments.setBackground(openedColor);
        assignmentOpen = true;
      break;
      default:
        settings.setBackground(openedColor);
        settingsOpen = true;
    }
    revalidate();
    repaint();
  }
  private void regularBackground() {
    lectureOpen = false;
    sectionOpen = false;
    assignmentOpen = false;
    settingsOpen = false;
    lectures.setBackground(closedColor);
    sections.setBackground(closedColor);
    assignments.setBackground(closedColor);
    settings.setBackground(closedColor);
  }
  

  public void refresh() {
    mainPanel.removeAll();
    mainPanel.revalidate();
    SubjectController.getInstance().refresh();
    subs = SubjectController.getInstance().getSubjects();
    System.out.println("size" + subs.size());
    subcontainer = new SubjectsContainer(Optional.ofNullable(checkAndGet(Open.Lecture)), path, Open.Lecture);
    seccontainer = new SubjectsContainer(Optional.ofNullable(checkAndGet(Open.Section)), path, Open.Section);
    assicontainer = new SubjectsContainer(Optional.ofNullable(checkAndGet(Open.Assignment)), path, Open.Assignment);
    settings.setVisible(false);
    changeBackgroundColor("lectures");
    mainPanel.add(subcontainer);
    if (!listener) {
      addListeners();
    }
    mainPanel.repaint();
  }
  private void addListeners() {
    lectures.addMouseListener(new MouseAdapter() {
      @Override
      public void mousePressed(MouseEvent e) {
        if (e.getButton() == 1) {
          changeBackgroundColor("lectures");
          mainPanel.removeAll();
          mainPanel.add(subcontainer);
          mainPanel.revalidate();
          mainPanel.repaint();
        }
      }
    });
    sections.addMouseListener(new MouseAdapter() {
      @Override
      public void mouseClicked(MouseEvent e) {
        if (e.getButton() == 1) {
          changeBackgroundColor("sections");
          mainPanel.removeAll();
          mainPanel.add(seccontainer);
          mainPanel.revalidate();
          mainPanel.repaint();
        }
      }
    });
    assignments.addMouseListener(new MouseAdapter() {
      @Override
      public void mouseClicked(MouseEvent e) {
        if (e.getButton() == 1) {
          changeBackgroundColor("assignments");
          mainPanel.removeAll();
          mainPanel.add(assicontainer);
          mainPanel.revalidate();
          mainPanel.repaint();
        }
      }
    });
    listener = true;
  }
  private boolean exists(Open open) {
    for (Subject sub: this.subs) {
      sub.setOpen(open);
      if (sub.exists()) {
        return true;
      }
    }
    return false;
  }
  private ArrayList<Subject> checkAndGet(Open open) {
    if (exists(open)) {
      return subs;
    }
    return null;
  }
